    private void listAlfombrasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listAlfombrasMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount()==2){
            
            //Cojo el indice
            int indice = listAlfombras.locationToIndex(evt.getPoint());
            
            //Cogemos la alfombra
            Alfombra a = modeloLista.getElementAt(indice);
            String alfombraSeleccionada = "MODELO: "+ a.getModelo() +"\n" +
                            "COR: "+ a.getCor() +"\n" +
                            "ANCHO: "+ a.getAncho()+" cm" +"\n" +
                            "ALTO: "+ a.getAlto()+" cm" +"\n\n" ;
            
            JOptionPane.showMessageDialog(this, alfombraSeleccionada);
        }
    }//GEN-LAST:event_listAlfombrasMouseClicked

