private boolean esNumero(String numero){
    if(numero.isBlank()){
        return false;
    }
    try{
        Double.parseDouble(numero);
        return true;
    }catch(NumberFormatException e){
        return false;
    }
    
}

private void limpiar(){
        txtModelo.setText("");
        txtCor.setText("");
        txtAncho.setText("");
        txtAlto.setText("");
}

