    private void buttonEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEjecutarActionPerformed
        // TODO add your handling code here:
        
        if(radioEliminar.isSelected()){
            
            String localidadSeleccionada = (String) modeloLocalidad.getSelectedItem();
        
            Provincia provinciaSeleccionada = (Provincia) modelo.getSelectedItem();
           
            if(localidadSeleccionada !=null && provinciaSeleccionada != null){
                
                modeloLocalidad.removeElement(localidadSeleccionada);
                
                provinciaSeleccionada.eliminarLocalidad(localidadSeleccionada);
            }
        }
        if(radioNueva.isSelected()){
            
            Provincia provinciaSeleccionada = (Provincia) modelo.getSelectedItem();
            
            if(txtLocalidad.getText().isBlank()){
                JOptionPane.showMessageDialog(this,"Rellene el campo Localidad para añadir una nueva");
            }else if(provinciaSeleccionada == null){
                JOptionPane.showMessageDialog(this,"Debe seleccionar una Provincia");
            }
            else{
                
                provinciaSeleccionada.añadirLocalidad(txtLocalidad.getText());
                
                modeloLocalidad.addElement(txtLocalidad.getText());
                
                JOptionPane.showMessageDialog(this,"Localidad añadida");
                txtLocalidad.setText("");
            }
        }
       
    }//GEN-LAST:event_buttonEjecutarActionPerformed

