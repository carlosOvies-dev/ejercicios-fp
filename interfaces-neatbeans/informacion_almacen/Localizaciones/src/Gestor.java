    private void comboProvinciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboProvinciaActionPerformed
        // TODO add your handling code here:
        Provincia provinciaSeleccionada = (Provincia) modelo.getSelectedItem();
        
        if(provinciaSeleccionada !=null){
             modeloLocalidad.removeAllElements();
             
             for(String localidad : provinciaSeleccionada.getLocalidad()){
                 modeloLocalidad.addElement(localidad); 
             }
             comboLocalidad.setSelectedIndex(0);
        }
    }//GEN-LAST:event_comboProvinciaActionPerformed

