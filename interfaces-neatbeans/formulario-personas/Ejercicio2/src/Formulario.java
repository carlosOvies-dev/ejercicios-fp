    private void butonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonGuardarActionPerformed
        
        //Lista para guardar los permisos
        List<Character> permisosConducir = new ArrayList<>();
        
        //Comprobar si hay algun permiso seleccionado
        boolean algunoSeleccionado = checkA.isSelected() ||
                                     checkB.isSelected() ||
                                     checkC.isSelected() ||
                                     checkD.isSelected() ||
                                     checkE.isSelected();
        //Titulacion seleccionada
        String tituloSeleccionado ="";
        
        //Genero
        String genero="";
        
        if(txtDni.getText().isBlank()){
            JOptionPane.showMessageDialog(this,"Complete el DNI");
        }
       else if(txtNombre.getText().isBlank()){
            JOptionPane.showMessageDialog(this,"Completa el Nombre");
        }
       else if(txtApellido1.getText().isBlank()){
            JOptionPane.showMessageDialog(this,"Completa el Apellido 1");
        }
       else if(txtApellido2.getText().isBlank()){
            JOptionPane.showMessageDialog(this,"Completa el Apellido 2");
        }
     
       else if(!algunoSeleccionado){
            JOptionPane.showMessageDialog(this,"Debes seleccionar almenos un permiso de conducir");
       }
       else if(radioOtra.isSelected() && txtOtra.getText().isBlank()){
            JOptionPane.showMessageDialog(this,"Si selecciona 'Otra', debes especificar la titulacion");
       }
       
       else{
           if(radioNinguna.isSelected()){
               tituloSeleccionado = "Ninguna";
           }
           else if(radioEso.isSelected()){
               tituloSeleccionado = "ESO";
           }
           else if(radioBachillerato.isSelected()){
               tituloSeleccionado ="Bachillerato";
           }
           else if(radioFp.isSelected()){
               tituloSeleccionado = "FP";
           }
           else if(radioUniversidad.isSelected()){
               tituloSeleccionado = "Universitaria";
           }
           else if(radioOtra.isSelected()){
               tituloSeleccionado = txtOtra.getText();
           }
           if(checkA.isSelected()){
               permisosConducir.add('A'); 
           }
           if(checkB.isSelected()){
               permisosConducir.add('B');
           }
           if(checkC.isSelected()){
               permisosConducir.add('C');
           }
           if(checkD.isSelected()){
               permisosConducir.add('D');
           }
           if(checkE.isSelected()){
               permisosConducir.add('E');
           }
           if(radioHombre.isSelected()){
               genero = "Hombre";
           }else{
               genero = "Mujer";
           }
           
           //Comprobacion dni
           String dniComprobar = txtDni.getText();
           boolean repetido = false;
           for(Persona p : listaPersonas){
               if(p.getDni().equalsIgnoreCase(dniComprobar)){
                   repetido =true;
                   break;
               }
           }
           if(repetido){
               JOptionPane.showMessageDialog(this,"Ya existe una persona con ese DNI en el registro");
           }
           else{
                 Persona nuevaPersona = new Persona(txtDni.getText(), txtNombre.getText(), txtApellido1.getText(),
                                           txtApellido2.getText(),genero , permisosConducir, tituloSeleccionado);
                    this.listaPersonas.add(nuevaPersona);
                  
                    //Mensaje para confirmar que esta correcto el añadido
                    JOptionPane.showMessageDialog(this, "Persona guardada con éxito.");
                    //Limpiamos formulario al añadir la persona
                    limpiarFormulario();
           }
           
           
      
       }
       
    }//GEN-LAST:event_butonGuardarActionPerformed

