    private void butonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonBuscarActionPerformed
        
        
        if(txtBuscar.getText().isBlank()){
            JOptionPane.showMessageDialog(this,"Debe introducir un dni");
        }else{
            //Buscamos a la persona
            String dniBuscar = txtBuscar.getText();
            boolean encontrada=false;
            Persona buscada = null;
            for(Persona p : listaPersonas){
                if(p.getDni().equalsIgnoreCase(dniBuscar)){
                    encontrada =true;
                    buscada = p;
                    break;
                }
            }
            //Si la encontramos empezamos a rellenar el formulario
            if(encontrada){
                //Limpiamos
                limpiarFormulario();
                //Cubrimos campos
                txtDni.setText(buscada.getDni());
                txtNombre.setText(buscada.getNombre());
                txtApellido1.setText(buscada.getApellido1());
                txtApellido2.setText(buscada.getApellido2());
                //Generos
                if(buscada.getGenero().equals("Hombre")){
                    radioHombre.setSelected(true);
                }else{
                    radioMujer.setSelected(true);
                }
                //Permisos
                for(Character permiso : buscada.getPermiso()){
                    
                    if(permiso == 'A'){
                        checkA.setSelected(true);
                    }
                    if(permiso == 'B'){
                        checkB.setSelected(true);
                    }
                    if(permiso == 'C'){
                        checkC.setSelected(true);
                    }
                    if(permiso == 'D'){
                        checkD.setSelected(true);
                    }
                    if(permiso == 'E'){
                        checkE.setSelected(true);
                    }
                }
                //Estudios
                String estudios = buscada.getTitulacion();
                
                if(estudios.equals("ESO")){
                    radioEso.setSelected(true);
                } 
                else if(estudios.equals("Bachillerato")){
                    radioBachillerato.setSelected(true);
                }
                else if(estudios.equals("FP")){
                    radioFp.setSelected(true);
                }
                else if(estudios.equals("Universitaria")){
                    radioUniversidad.setSelected(true);
                }
                else if(estudios.equals("Ninguna")){
                    radioNinguna.setSelected(true);
                }else{
                    radioOtra.setSelected(true);
                    txtOtra.setEnabled(true);
                    txtOtra.setText(buscada.getTitulacion());
                }
                
                
            } else{
                JOptionPane.showMessageDialog(this, "No se encontró ninguna persona con ese DNI.");
            }
        }
    }//GEN-LAST:event_butonBuscarActionPerformed

