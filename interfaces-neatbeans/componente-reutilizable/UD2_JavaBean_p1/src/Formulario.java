    private void buttomGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttomGuardarActionPerformed
        // TODO add your handling code here:
       if(txtDNI.getText().isBlank()){
           fallo("Complete el dni");
           return;
       }
        if(txtNombre.getText().isBlank()){
            fallo("Complete el nombre");
            return;
        }
        if(txtApellido1.getText().isBlank()){
            fallo("Complete el Apellido 1");
                    return;
        }
        if(txtApellido2.getText().isBlank()){
            fallo("Complete el Apellido 2");
            return;
        }
        String dni = txtDNI.getText();
        String nombre = txtNombre.getText();
        String apellido1 = txtApellido1.getText();
        String apellido2 = txtApellido2.getText();
        
        List<Character> lista = new ArrayList<>();
        boolean marcado=false;
        if(boxA.isSelected()){
            lista.add('A');
            marcado=true;
        }
        if(boxB.isSelected()){
            lista.add('B');
            marcado=true;
        }
        if(boxC.isSelected()){
            lista.add('C');
            marcado=true;
        }
        if(boxD.isSelected()){
            lista.add('D');
            marcado=true;
        }
        if(marcado==false){
            fallo("Marque un carnet");
            return;
        }
        String genero="";
        if(radioHombre.isSelected()){
            genero="Hombre";
        }else{
            genero="Mujer";
        }
        String estudios="";
        if(radioNinguna.isSelected()){
            estudios="Ninguna";
        }
        if(radioESO.isSelected()){
            estudios="ESO";
        }
        if(radioFP.isSelected()){
            estudios="FP";
        }
        if(radioBachillerato.isSelected()){
            estudios="Bachillerato";
        }
        if(radioOtra.isSelected()){
            
            if(txtOtra.getText().isBlank()){
                fallo("Complete el campo de Otra");
                return;
            }
            estudios= txtOtra.getText();
        }
        Datos dato = new Datos(dni, nombre,apellido1, apellido2, lista, genero, estudios);
        
        exito(dato);
    }//GEN-LAST:event_buttomGuardarActionPerformed

