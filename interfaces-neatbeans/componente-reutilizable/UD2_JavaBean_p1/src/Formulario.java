    public void addListener(EventoListener listener){
        miListener.add(listener);
    }
    
    public void borrarListener(EventoListener listener){
        miListener.remove(listener);
    }
    
    public void fallo(String error){
        FalloEvent fail = new FalloEvent(this,error );
        
        for(EventoListener l : miListener){
            l.fallido(fail);
        }
    }
    
    public void exito(Datos dato){
        ExitoEvent exito = new ExitoEvent(this, dato);
        for(EventoListener l : miListener){
            l.exitoso(exito);
        }
    }

