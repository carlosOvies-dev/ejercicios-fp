    private void buttonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMostrarActionPerformed
        // TODO add your handling code here:
        boolean algunoSeleccionado = radioMostrarA.isSelected() ||
                                     radioMostrarB.isSelected() ||
                                     radioMostarC.isSelected() ||
                                     radioMostrarTodas.isSelected();
        if(!algunoSeleccionado){
            JOptionPane.showMessageDialog(this,"Seleccione un tipo para mostrar");
        }else{
            
            if(radioMostrarA.isSelected()){
                for(Hija a : listaTipoA){
                    a.setVisible(true);
                }
            }
            if(radioMostrarB.isSelected()){
                for(Hija b : listaTipoB){
                    b.setVisible(true);
                }
            }
            if(radioMostarC.isSelected()){
                for(Hija c : listaTipoC){
                    c.setVisible(true);
                }
            }
            if(radioMostrarTodas.isSelected()){
                  for(Hija a : listaTipoA){
                    a.setVisible(true);
                }
                   for(Hija b : listaTipoB){
                    b.setVisible(true);
                }
                   for(Hija c : listaTipoC){
                    c.setVisible(true);
                }
            }
            listVentanas.repaint();
        }
    }//GEN-LAST:event_buttonMostrarActionPerformed

