    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(modeloListaHijas.isEmpty()){
             JOptionPane.showMessageDialog(this,"La lista esta vacia");
            return;
        }
        List<Hija> ventanasSeleccionadas = listVentanas.getSelectedValuesList();
        
        if(ventanasSeleccionadas.isEmpty()){
            JOptionPane.showMessageDialog(this,"Selecciona almenos una ventana");
        }else{
            for(Hija a : ventanasSeleccionadas){
                a.dispose();
                modeloListaHijas.removeElement(a);
                if(a.getTitle().startsWith("A")){
                    listaTipoA.remove(a);
                }else if(a.getTitle().startsWith("B")){
                    listaTipoB.remove(a);
                }else if(a.getTitle().startsWith("C")){
                    listaTipoC.remove(a);
                }
            }
            acualizarContador();
            listVentanas.repaint();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

