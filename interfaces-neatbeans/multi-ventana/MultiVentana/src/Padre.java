    private void buttonHijaAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHijaAActionPerformed
        // TODO add your handling code here:
        if(listaTipoA.size()< limiteListas){
            Hija hijaA = new Hija(this,false);
            hijaA.setLocationRelativeTo(null);
            hijaA.setVisible(true);
            //Contador para el numero de ventana
            this.contadorIDs++;
            int numeroVentana = this.contadorIDs;
            hijaA.setTitle("A-"+numeroVentana);
            hijaA.setLabelHija("VENTANA TIPO A");
            listaTipoA.add(hijaA);
            modeloListaHijas.addElement(hijaA);
            acualizarContador();
        }else{
            JOptionPane.showMessageDialog(this,"Limite de Hijas Tipo A alcanzado");
        }
        
        
    }//GEN-LAST:event_buttonHijaAActionPerformed

