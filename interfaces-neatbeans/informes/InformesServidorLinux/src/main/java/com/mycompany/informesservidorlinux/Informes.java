    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
    try {
        java.io.InputStream reportStream = getClass().getResourceAsStream("/informes/Ejercicio7.jrxml");
        
        if (reportStream == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: No se encuentra /informes/Ejercicio7.jrxml");
            return;
        }

       
        java.net.URL urlOK = getClass().getResource("/imagenes/ok.png");
        java.net.URL urlKO = getClass().getResource("/imagenes/ko.png");

       
        if (urlOK == null || urlKO == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: No se encuentran las imágenes en la carpeta /imagenes/");
            return;
        }

      
        java.util.Map<String, Object> parametros = new java.util.HashMap<>();
        parametros.put("miNombre", MI_NOMBRE);
        
       
        parametros.put("imagenOK", urlOK.toString());
        parametros.put("imagenKO", urlKO.toString());

       
        net.sf.jasperreports.engine.JasperReport report = net.sf.jasperreports.engine.JasperCompileManager.compileReport(reportStream);
        net.sf.jasperreports.engine.JasperPrint print = net.sf.jasperreports.engine.JasperFillManager.fillReport(report, parametros, Conexion.getConexion());

        if (print.getPages().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "No hay datos para mostrar.");
        } else {
            net.sf.jasperreports.view.JasperViewer.viewReport(print, false);
        }

    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

