    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
     try {
            String minStr = javax.swing.JOptionPane.showInputDialog(this, "Introduce la edad MÍNIMA:");
            if (minStr == null) return;

            String maxStr = javax.swing.JOptionPane.showInputDialog(this, "Introduce la edad MÁXIMA:");
            if (maxStr == null) return;

            if (!minStr.isEmpty() && !maxStr.isEmpty()) {
                int edadMin = Integer.parseInt(minStr);
                int edadMax = Integer.parseInt(maxStr);

                java.io.InputStream reportStream = getClass().getResourceAsStream("/informes/Ejercicio5.jrxml");

                if (reportStream == null) {
                    javax.swing.JOptionPane.showMessageDialog(this, "No se encuentra el archivo /informes/Ejercicio5.jrxml");
                    return;
                }

                java.util.Map<String, Object> parametros = new java.util.HashMap<>();
                parametros.put("miNombre", MI_NOMBRE);
                parametros.put("edadMinima", edadMin);
                parametros.put("edadMaxima", edadMax);

                net.sf.jasperreports.engine.JasperReport report = net.sf.jasperreports.engine.JasperCompileManager.compileReport(reportStream);
                net.sf.jasperreports.engine.JasperPrint print = net.sf.jasperreports.engine.JasperFillManager.fillReport(report, parametros, Conexion.getConexion());

                if (print.getPages().isEmpty()) {
                    javax.swing.JOptionPane.showMessageDialog(this, "No hay datos para el rango seleccionado.");
                } else {
                    net.sf.jasperreports.view.JasperViewer.viewReport(print, false);
                }
            }
        } catch (NumberFormatException nfe) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, introduce solo números válidos.");
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

